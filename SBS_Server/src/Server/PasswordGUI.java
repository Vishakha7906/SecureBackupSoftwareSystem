    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(getInfo()){
            if(setting.getBoolValue("sbsPassActiveServer")){
                enterPass.setVisible(true);
                if(setting.getBoolValue("sbsPassCorrectServer")){
                    setting.SaveSetting("string", "sbsPassServer", pass);
                    setting.SaveSetting("bool", "sbsPassActiveServer", "true");
                    JLabel msgLabel= new JLabel();
                    msgLabel.setFont(new Font ("Microsoft tai le", Font.BOLD, 16));
                    msgLabel.setText("Password changed");
                    JOptionPane.showMessageDialog(rootPane, msgLabel, null, HEIGHT, new ImageIcon(getClass().getResource(infoIcon)));
                }
                this.dispose();
                return;
            }
            JLabel msgLabel= new JLabel();
            msgLabel.setFont(new Font ("Microsoft tai le", Font.BOLD, 16));
            msgLabel.setText("Password changed");
            JOptionPane.showMessageDialog(rootPane, msgLabel, null, HEIGHT, new ImageIcon(getClass().getResource(infoIcon)));
            
            setting.SaveSetting("string", "sbsPassServer", pass);
            setting.SaveSetting("bool", "sbsPassActiveServer", "true");
            
            this.dispose();
        }
        refresh();
    }//GEN-LAST:event_btnSaveActionPerformed

