    private void labelPassOnOffMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelPassOnOffMouseClicked
        boolean passOn= setting.getBoolValue("sbsPassActiveServer");
        String getPass= setting.getStingValue("sbsPassServer");
        if(passOn){
            enterPass.setVisible(true);
            if(setting.getBoolValue("sbsPassCorrectServer")){
                setting.SaveSetting("bool", "sbsPassActiveServer", "false");
                setting.SaveSetting("string", "sbsPassServer", "");
            }
        }
        else{
            if(getPass!=null && getPass.length()>2){
                setting.SaveSetting("bool", "sbsPassActiveServer", "true");
            }
            else{
                JLabel msgLabel= new JLabel();
                msgLabel.setFont(new Font ("Microsoft tai le", Font.BOLD, 16));
                msgLabel.setText("Enter a new password");
                JOptionPane.showMessageDialog(rootPane, msgLabel, null, HEIGHT, new ImageIcon(getClass().getResource(errorIcon)));
            }
        }
        refresh();
    }//GEN-LAST:event_labelPassOnOffMouseClicked

