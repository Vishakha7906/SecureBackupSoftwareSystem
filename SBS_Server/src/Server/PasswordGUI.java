    private void refresh(){
        boolean passOn= setting.getBoolValue("sbsPassActiveServer");
        if(passOn){
            labelPassOnOff.setIcon(new ImageIcon(getClass().getResource(labelDeviceOn)));
        }
        else{
            labelPassOnOff.setIcon(new ImageIcon(getClass().getResource(labelDeviceOff)));
        }
    }
    
    private boolean getInfo(){
        pass= fieldPass.getText();
        if(pass.length()>0){
            if(pass.length()<4){
                JLabel msgLabel= new JLabel();
                msgLabel.setFont(new Font ("Microsoft tai le", Font.BOLD, 16));
                msgLabel.setText("Password must contains at least 4 characters");
                JOptionPane.showMessageDialog(rootPane, msgLabel, null, HEIGHT, new ImageIcon(getClass().getResource(errorIcon)));
                return false;
            }
            if(pass.length()>10){
                JLabel msgLabel= new JLabel();
                msgLabel.setFont(new Font ("Microsoft tai le", Font.BOLD, 16));
                msgLabel.setText("Password cannot contains more than 10 characters");
                JOptionPane.showMessageDialog(rootPane, msgLabel, null, HEIGHT, new ImageIcon(getClass().getResource(errorIcon)));
                return false;
            }
            return true;
        }
        JLabel msgLabel= new JLabel();
        msgLabel.setFont(new Font ("Microsoft tai le", Font.BOLD, 16));
        msgLabel.setText("Enter a new password before Save");
        JOptionPane.showMessageDialog(rootPane, msgLabel, null, HEIGHT, new ImageIcon(getClass().getResource(errorIcon)));
        return false;
    }

