    private void refresh(){
        String url= setting.getStingValue("serverUrl");
        if(url.length()>40){
            url= url.substring(0, 39);
            labelSavedURL.setText(url + "...");
        }
        else labelSavedURL.setText(url);
    }
    
    private boolean getInfo(){
        String url= fieldURL.getText();
        if(url.length()<2) return false;
        setting.SaveSetting("string", "serverUrl", url);
        return true;
    }

