    private void btnUpExpireActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpExpireActionPerformed
        if(expireDateStr!=null){
            rct.editUsrExpire(userId, expireDateStr);
            labelDate.setText(expireDateStr);
        }
        else{
            expireDateStr= txtFieldCus.getText();
            if(expireDateStr!=null || expireDateStr.length()>2){
                rct.editUsrExpire(userId, expireDateStr);
                labelDate.setText(expireDateStr);
            }
            else{
                JLabel msgLabel= new JLabel();
                msgLabel.setFont(new Font ("Microsoft tai le", Font.BOLD, 16));
                msgLabel.setText("Invalid date");
                JOptionPane.showMessageDialog(rootPane, msgLabel, null, HEIGHT, new ImageIcon(getClass().getResource(errorIcon)));
            }
        }
    }//GEN-LAST:event_btnUpExpireActionPerformed

